    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(tblTransaksi.getSelectionModel().isSelectionEmpty()){
            JOptionPane.showMessageDialog(this, "Silahkan pilih data yang akan dihapus!");
            kosongkanForm();
        }else {
            DefaultTableModel model = (DefaultTableModel)tblTransaksi.getModel();
            int row = tblTransaksi.getSelectedRow();
            
            Transaksi tr = new Transaksi().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
            tr.delete();
            kosongkanForm();
            tampilkanDataTransaksi();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        // TODO add your handling code here:
        int totalHarga;
        int jumlah;
        int harga;
        int diskon;
        jumlah = Integer.parseInt(txtJumlah.getText());
        harga = Integer.parseInt(txtHargaObat.getText());
        Pembeli pe = cariPembeli(Integer.parseInt(txtIdPembeli.getText()));
        if (pe.getIdMember() != 0){
            diskon = (harga*5/100)*jumlah;
            totalHarga = (jumlah * harga) - diskon;
            txtTotal.setText("" + totalHarga);
            txtDiskon.setText("Diskon: " + diskon);
        } else {
            totalHarga = (jumlah * harga);
            txtTotal.setText("" + totalHarga);
            txtDiskon.setText("Diskon: 0");
        }
    }//GEN-LAST:event_btnTotalActionPerformed

