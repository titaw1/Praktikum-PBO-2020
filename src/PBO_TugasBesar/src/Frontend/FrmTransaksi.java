    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Transaksi tr = new Transaksi();   
        Kasir ka = cariKasir(Integer.parseInt(txtIdKasir.getText()));
        Pembeli pe = cariPembeli(Integer.parseInt(txtIdPembeli.getText()));
        Obat ob = cariObat(Integer.parseInt(txtIdObat.getText()));
        Kasir newka = new Kasir().getById(ka.getIdKasir());
        Pembeli newpe = new Pembeli().getById(pe.getIdPembeli());
        Obat newob = new Obat().getById(ob.getIdObat());
        tr.setIdTransaksi(Integer.parseInt(txtTransaksi.getText()));
        tr.setTanggalTransaksi(txtTanggalTransaksi.getText());
        tr.setKasir(newka);
        tr.setPembeli(newpe);
        tr.setObat(newob);
        tr.setJumlah(Integer.parseInt(txtJumlah.getText()));
        tr.setTotalBiaya(Integer.parseInt(txtTotal.getText()));
        tr.save();
        tampilkanDataTransaksi();
    }//GEN-LAST:event_btnSimpanActionPerformed

