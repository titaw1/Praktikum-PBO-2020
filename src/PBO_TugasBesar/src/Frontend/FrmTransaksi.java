    private void btnCariKasirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariKasirActionPerformed
        // TODO add your handling code here:
        if(txtIdKasir.getText().equals("")){
            JOptionPane.showMessageDialog(this, "ID Kasir harus diisi !"); 
        }else {
            Kasir ka = cariKasir(Integer.parseInt(txtIdKasir.getText()));
            if(ka.getIdKasir() != 0){
                txtIdKasir.setText(String.valueOf(ka.getIdKasir()));
                txtNamaKasir.setText(ka.getNamaKasir());
            } else {
                JOptionPane.showMessageDialog(this, "ID Kasir tidak ditemukan!"); 
                txtIdKasir.setText("");
                txtNamaKasir.setText("Nama Kasir");
            }
        }
    }//GEN-LAST:event_btnCariKasirActionPerformed

