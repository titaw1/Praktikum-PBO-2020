    public final void tampilkanDataTransaksi(){ 
        String[] kolom = {"ID", "Tanggal Transaksi", "Nama Kasir", "Nama Pembeli", "Obat", "Jumlah", "Total Biaya"};
        ArrayList<Transaksi> list = new Transaksi().getAll();
        Object rowData[] = new Object[7];
        
        tblTransaksi.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Transaksi tr : list ) {
            rowData[0] = tr.getIdTransaksi();
            rowData[1] = tr.getTanggalTransaksi();
            rowData[2] = tr.getKasir().getNamaKasir();
            rowData[3] = tr.getPembeli().getNamaPembeli();
            rowData[4] = tr.getObat().getNamaObat();
            rowData[5] = tr.getJumlah();
            rowData[6] = tr.getTotalBiaya();
        
            ((DefaultTableModel)tblTransaksi.getModel()).addRow(rowData);
        }
    }
    
